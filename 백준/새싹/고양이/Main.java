package 새싹.고양이;

public class Main {
    /*
   자바에서 특수문자를 출력 시 이스케이프 문자를 어떻게 사용하는지를 익힐 수 있는 좋은 문제다
   역슬래시( \ )는 Java 문자열에서 \\ 로 이스케이프해야 함
    */
    public static void main(String[] args) {
        System.out.println("\\    /\\");
        System.out.println(" )  ( ')");
        System.out.println("(  /  )");
        System.out.println(" \\(__)|");
    }
}
