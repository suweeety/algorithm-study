import java.util.Scanner;
/*
* int는 4byte
* long은 8byte
*/
public class 꼬마정민 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        long a,b,c;
        a = scanner.nextLong();
        b = scanner.nextLong();
        c = scanner.nextLong();

        System.out.println(a+b+c);
    }
}
